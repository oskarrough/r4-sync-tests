<script>
	import {addTrack, updateTrack} from '$lib/tanstack/collections/tracks'
	import {fetchOEmbedTitle} from '$lib/utils/oembed'
	import * as m from '$lib/paraglide/messages'

	const uid = $props.id()

	/** @type {{mode: 'create', channel: import('$lib/tanstack/collections/channels').Channel, trackId?: string, url?: string, title?: string, description?: string, discogs_url?: string, onsubmit?: (event: {data: {url: string, title: string} | null, error: Error | null}) => void} | {mode: 'edit', channel: {id: string, slug: string}, trackId: string, url?: string, title?: string, description?: string, discogs_url?: string, onsubmit?: (event: {data: {url: string, title: string} | null, error: Error | null}) => void}} */
	let {
		mode,
		channel,
		trackId,
		url: initialUrl = '',
		title: initialTitle = '',
		description: initialDescription = '',
		discogs_url: initialDiscogsUrl = '',
		onsubmit
	} = $props()

	let error = $state('')
	let submitting = $state(false)
	let fetchingTitle = $state(false)

	/** @type {HTMLInputElement | undefined} */
	let titleInput = $state()
	/** @type {HTMLTextAreaElement | undefined} */
	let descriptionInput = $state()
	let pendingUrl = ''

	async function handleUrlInput(event) {
		const input = /** @type {HTMLInputElement} */ (event.target)
		if (!input.validity.valid || !input.value || titleInput?.value) return
		const url = input.value
		pendingUrl = url
		fetchingTitle = true
		try {
			const fetched = await fetchOEmbedTitle(url)
			if (fetched && titleInput && !titleInput.value && url === pendingUrl) {
				titleInput.value = fetched
			}
		} finally {
			if (url === pendingUrl) fetchingTitle = false
		}
	}

	/** @param {Event} event */
	function handleDiscogsSuggestion(event) {
		const tags = /** @type {CustomEvent<string[]>} */ (event).detail
		const hashtags = tags.map((t) => `#${t}`).join(' ')
		if (descriptionInput) {
			descriptionInput.value = descriptionInput.value ? `${descriptionInput.value} ${hashtags}` : hashtags
		}
	}

	/** @param {SubmitEvent} event */
	async function handleSubmit(event) {
		event.preventDefault()
		if (submitting) return

		const formData = new FormData(/** @type {HTMLFormElement} */ (event.target))
		const url = /** @type {string} */ (formData.get('url'))
		const title = /** @type {string} */ (formData.get('title'))
		const description = /** @type {string} */ (formData.get('description'))
		const discogs_url = /** @type {string} */ (formData.get('discogs_url'))

		if (!url || !title) return

		error = ''
		submitting = true

		try {
			if (mode === 'create') {
				await addTrack(channel, {
					url,
					title,
					description: description || undefined,
					discogs_url: discogs_url || undefined
				})
			} else {
				if (!trackId) throw new Error('Track ID required for update')

				// Skip update if nothing changed
				const hasChanges =
					url !== initialUrl ||
					title !== initialTitle ||
					description !== initialDescription ||
					discogs_url !== initialDiscogsUrl

				if (hasChanges) {
					await updateTrack(channel, trackId, {
						url,
						title,
						description: description || undefined,
						discogs_url: discogs_url || undefined
					})
				}
			}
			onsubmit?.({data: {url, title}, error: null})
			if (mode === 'create') {
				/** @type {HTMLFormElement} */ event.target.reset()
			}
		} catch (err) {
			error = /** @type {Error} */ (err).message || 'Failed to save track'
			onsubmit?.({data: null, error: /** @type {Error} */ (err)})
		} finally {
			submitting = false
		}
	}

	const discogsUrl = $derived(initialDiscogsUrl)
	const isValidDiscogsUrl = $derived(discogsUrl?.includes('discogs.com'))
</script>

{#if error}
	<p class="error" role="alert">{m.common_error()}: {error}</p>
{/if}

<form class="form" onsubmit={handleSubmit} disabled={submitting}>
	<fieldset>
		<legend><label for="{uid}-url">URL</label></legend>
		<input
			id="{uid}-url"
			name="url"
			type="url"
			value={initialUrl}
			oninput={handleUrlInput}
			required
			placeholder="https://..."
		/>
	</fieldset>

	<fieldset>
		<legend><label for="{uid}-title">Title {fetchingTitle ? '...' : ''}</label></legend>
		<input
			bind:this={titleInput}
			id="{uid}-title"
			name="title"
			type="text"
			value={initialTitle}
			required
			placeholder="Track title"
		/>
	</fieldset>

	<fieldset>
		<legend><label for="{uid}-description">Description</label></legend>
		<textarea
			bind:this={descriptionInput}
			id="{uid}-description"
			name="description"
			rows="2"
			placeholder="Optional description">{initialDescription}</textarea
		>
	</fieldset>

	<fieldset>
		<legend><label for="{uid}-discogs_url">Discogs URL</label></legend>
		<input
			id="{uid}-discogs_url"
			name="discogs_url"
			type="url"
			value={initialDiscogsUrl}
			placeholder="https://discogs.com/release/..."
		/>
	</fieldset>

	{#if isValidDiscogsUrl}
		<r4-discogs-resource url={discogsUrl} suggestions="true" onsuggestion={handleDiscogsSuggestion}
		></r4-discogs-resource>
	{/if}

	<button type="submit" disabled={submitting}>
		{submitting ? m.common_save() + '...' : mode === 'create' ? m.track_add_title() : m.common_save()}
	</button>
</form>

<style>
	r4-discogs-resource {
		display: block;
		padding-left: 0.5rem;
		min-height: 6rem;
		font-size: var(--font-4);
		font-style: italic;

		:global(fieldset) {
			flex-flow: row wrap;
			font-style: normal;
		}
		:global(legend) {
			float: left;
		}
	}
</style>
